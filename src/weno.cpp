std::vector<Conserved> WENO1d::make_outflow_Ubc(const std::vector<Conserved>& U, int Nghost) {
    const int nx = (int)U.size();
    std::vector<Conserved> Ubc(nx + 2 * Nghost);

    for (int g = 0; g < Nghost; ++g) Ubc[g] = U[0];
    for (int i = 0; i < nx; ++i)    Ubc[Nghost + i] = U[i];
    for (int g = 0; g < Nghost; ++g) Ubc[Nghost + nx + g] = U[nx - 1];

    return Ubc;
}

std::vector<Conserved> WENO1d::make_periodic_Ubc(const std::vector<Conserved>& U, int Nghost) {
    const int nx = (int)U.size();
    std::vector<Conserved> Ubc(nx + 2 * Nghost);

    for (int i = 0; i < nx + 2 * Nghost; ++i) {
        int idx = (i - Nghost) % nx;
        if (idx < 0) idx += nx;
        Ubc[i] = U[idx];
    }
    return Ubc;
}

VectOfVectDouble WENO1d::conserved_to_matrix(const std::vector<Conserved>& U) {
    const int n = (int)U.size();
    VectOfVectDouble M(n, VectOfDouble(3, 0.0));
    for (int i = 0; i < n; ++i) {
        M[i][0] = U[i].rho;
        M[i][1] = U[i].mom;
        M[i][2] = U[i].E;
    }
    return M;
}

// ====================================================================
//  In-place / buffer-reuse methods
// ====================================================================

void WENO1d::fill_periodic_Ubc(const std::vector<Conserved>& U, int Nghost,
                                std::vector<Conserved>& Ubc)
{
    const int nx = (int)U.size();
    const int nTotal = nx + 2 * Nghost;
    if ((int)Ubc.size() != nTotal) Ubc.resize(nTotal);

    for (int i = 0; i < nTotal; ++i) {
        int idx = (i - Nghost) % nx;
        if (idx < 0) idx += nx;
        Ubc[i] = U[idx];
    }
}

void WENO1d::fill_outflow_Ubc(const std::vector<Conserved>& U, int Nghost,
                               std::vector<Conserved>& Ubc)
{
    const int nx = (int)U.size();
    const int nTotal = nx + 2 * Nghost;
    if ((int)Ubc.size() != nTotal) Ubc.resize(nTotal);

    for (int g = 0; g < Nghost; ++g) Ubc[g] = U[0];
    for (int i = 0; i < nx; ++i)    Ubc[Nghost + i] = U[i];
    for (int g = 0; g < Nghost; ++g) Ubc[Nghost + nx + g] = U[nx - 1];
}

void WENO1d::fill_matrix(const std::vector<Conserved>& Ubc,
                          VectOfVectDouble& mat)
{
    const int n = (int)Ubc.size();
    if ((int)mat.size() != n) {
        mat.assign(n, VectOfDouble(3, 0.0));
    }
    for (int i = 0; i < n; ++i) {
        mat[i][0] = Ubc[i].rho;
        mat[i][1] = Ubc[i].mom;
        mat[i][2] = Ubc[i].E;
    }
}

WENOWorkspace WENO1d::alloc_workspace(int nx) const {
    WENOWorkspace ws;
    const int Nghost = requiredGhost();
    const int nTotal = nx + 2 * Nghost;
    ws.Ubc_cons.resize(nTotal);
    ws.Ubc_mat.assign(nTotal, VectOfDouble(3, 0.0));
    ws.PolyL.resize(nx);
    ws.PolyR.resize(nx);
    return ws;
}

void WENO1d::reconstruct_all_interfaces(
    const std::vector<Conserved>& U,
    WenoBCType bc,
    WENOWorkspace& ws
) const {
    const int nx = (int)U.size();
    const int Nghost = requiredGhost();

    if (bc == WenoBCType::Periodic)
        fill_periodic_Ubc(U, Nghost, ws.Ubc_cons);
    else
        fill_outflow_Ubc(U, Nghost, ws.Ubc_cons);

    fill_matrix(ws.Ubc_cons, ws.Ubc_mat);

    if ((int)ws.PolyL.size() != nx) ws.PolyL.resize(nx);
    if ((int)ws.PolyR.size() != nx) ws.PolyR.resize(nx);

    #pragma omp parallel for schedule(static)
    for (int i_interface = 0; i_interface < nx; ++i_interface) {
        VectOfVectDouble WL, WR;
        reconstruct_for_flux(ws.Ubc_mat, i_interface, WL, WR, Nghost);

        InterfacePoly left{}, right{};

        left.Q   = Conserved{WL[0][0], WL[0][1], WL[0][2]};
        right.Q  = Conserved{WR[0][0], WR[0][1], WR[0][2]};

        if (m_M >= 1) {
            left.Qx  = Conserved{WL[1][0], WL[1][1], WL[1][2]};
            right.Qx = Conserved{WR[1][0], WR[1][1], WR[1][2]};
        }
        if (m_M >= 2) {
            left.Qxx  = Conserved{WL[2][0], WL[2][1], WL[2][2]};
            right.Qxx = Conserved{WR[2][0], WR[2][1], WR[2][2]};
        }
        if (m_M >= 3) {
            left.Qxxx  = Conserved{WL[3][0], WL[3][1], WL[3][2]};
            right.Qxxx = Conserved{WR[3][0], WR[3][1], WR[3][2]};
        }
        if (m_M >= 4) {
            left.Qxxxx  = Conserved{WL[4][0], WL[4][1], WL[4][2]};
            right.Qxxxx = Conserved{WR[4][0], WR[4][1], WR[4][2]};
        }

        // Positivity-preserving fallback: if reconstructed interface value
        // has negative density or negative pressure, fall back to cell average
        // (piecewise constant, zero derivatives). This prevents blowup near shocks.
        auto is_physical = [](const Conserved& Q) -> bool {
            if (Q.rho <= 0.0 || !std::isfinite(Q.rho)) return false;
            double p = (Gamma - 1.0) * (Q.E - 0.5 * Q.mom * Q.mom / Q.rho);
            return p > 0.0 && std::isfinite(p);
        };

        if (!is_physical(left.Q)) {
            // Fall back to cell average of left cell (index i_interface - 1)
            int iL_ghost = i_interface + Nghost - 1;
            left.Q = Conserved{ws.Ubc_mat[iL_ghost][0], ws.Ubc_mat[iL_ghost][1], ws.Ubc_mat[iL_ghost][2]};
            left.Qx = left.Qxx = left.Qxxx = left.Qxxxx = Conserved{0,0,0};
        }

        if (!is_physical(right.Q)) {
            // Fall back to cell average of right cell (index i_interface)
            int iR_ghost = i_interface + Nghost;
            right.Q = Conserved{ws.Ubc_mat[iR_ghost][0], ws.Ubc_mat[iR_ghost][1], ws.Ubc_mat[iR_ghost][2]};
            right.Qx = right.Qxx = right.Qxxx = right.Qxxxx = Conserved{0,0,0};
        }

        ws.PolyL[i_interface] = left;
        ws.PolyR[i_interface] = right;
    }
}


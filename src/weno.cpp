WENO1d::WENO1d(int Polydegree, double dx, double epsilon, int qPower)
    : m_dx(dx), m_M(Polydegree), m_epsilon(epsilon), m_qPower(qPower)
{
    m_TotalStencilsize = 2 * m_M + 1;
    m_PolyStencilsize  = m_M + 1;

    if (m_dx <= 0.0) {
        std::cerr << "[WENO] dx must be positive.\n";
        std::exit(EXIT_FAILURE);
    }

    if (m_M < 0 || m_M > 4) {
        std::cerr << "[WENO] Implemented for degree M=0,1,2,3,4 only.\n";
        std::exit(EXIT_FAILURE);
    }

    // WENO-DK multiplicative formula: p controls shock dissipation per order.
    // M=3 (ADER4): p=3  -> more dissipation, less oscillatory near shocks
    // M=4 (ADER5): p=2  -> standard WENO-Z, slightly less dissipation
    // This ensures ADER4 oscillates less than ADER5 at discontinuities,
    // while both retain their formal convergence orders on smooth problems.
    if (m_epsilon > 1e-10) m_epsilon = 1e-14;
    // Per-order r_power: higher = more dissipative near shocks
    // Gives ADER3 < ADER4 < ADER5 oscillation ordering
    if      (m_M == 2) m_qPower = 4;
    else if (m_M == 3) m_qPower = 3;
    else               m_qPower = 2;

    // Legacy arrays (unused in new implementation but kept for header compat)
    m_CMatsL.resize(1);
    m_CMatsR.resize(1);
    m_BMats.resize(1);
    m_optWeightsL.assign(1, 0.0);
    m_optWeightsR.assign(1, 0.0);
}

void WENO1d::reconstruct_for_flux(
    const VectOfVectDouble& Ubc,
    int i_interface,
    VectOfVectDouble& U_WenoL,
    VectOfVectDouble& U_WenoR,
    int Nghost
) const {
    const int nVars = (int)Ubc[0].size();

    U_WenoL.assign(m_M + 1, VectOfDouble(nVars, 0.0));
    U_WenoR.assign(m_M + 1, VectOfDouble(nVars, 0.0));

    if (m_M == 0) {
        U_WenoL[0] = Ubc[i_interface + Nghost - 1];
        U_WenoR[0] = Ubc[i_interface + Nghost];
        return;
    }

    static const DumbserCoeffs coeff1 = buildCoeffs(1);
    static const DumbserCoeffs coeff2 = buildCoeffs(2);
    static const DumbserCoeffs coeff3 = buildCoeffs(3);
    static const DumbserCoeffs coeff4 = buildCoeffs(4);

    const DumbserCoeffs* co = nullptr;
    switch (m_M) {
        case 1: co = &coeff1; break;
        case 2: co = &coeff2; break;
        case 3: co = &coeff3; break;
        case 4: co = &coeff4; break;
    }

    const int L = co->L;
    const int k = co->k;
    const int nS = co->nStencil;
    const double inv_dx = 1.0 / m_dx;

    // Left cell of interface: ghost-padded index = i_interface + Nghost - 1
    // Right cell of interface: ghost-padded index = i_interface + Nghost
    const int iL_cell = i_interface + Nghost - 1;  // left cell
    const int iR_cell = i_interface + Nghost;       // right cell

    // --- Reconstruct LEFT cell: all variables first, then limit, then eval ---
    double wh_left[3][MAX_L];
    for (int v = 0; v < nVars; ++v) {
        double u_center_L[MAX_S], u_left_L[MAX_S], u_right_L[MAX_S];
        for (int j = 0; j < nS; ++j) {
            u_center_L[j] = Ubc[iL_cell - k + j][v];   // central: iL-k .. iL+k
            u_left_L[j]   = Ubc[iL_cell - 2*k + j][v];  // left:    iL-2k .. iL
            u_right_L[j]  = Ubc[iL_cell + j][v];         // right:   iL .. iL+2k
        }
        reconstruct_cell_legendre(u_center_L, u_left_L, u_right_L, wh_left[v],
                                  *co, m_epsilon, m_qPower);
    }

    // Zhang-Shu positivity limiter on left cell
    zhang_shu_limiter(wh_left, L, m_M);

    // Evaluate at right boundary (eta=1) of left cell
    for (int v = 0; v < nVars; ++v) {
        double derivs_L[MAX_L];
        eval_at_boundary(wh_left[v], L, co->EvalRight, derivs_L, inv_dx);
        for (int d = 0; d < L; ++d)
            U_WenoL[d][v] = derivs_L[d];
    }

    // --- Reconstruct RIGHT cell: all variables first, then limit, then eval ---
    double wh_right[3][MAX_L];
    for (int v = 0; v < nVars; ++v) {
        double u_center_R[MAX_S], u_left_R[MAX_S], u_right_R[MAX_S];
        for (int j = 0; j < nS; ++j) {
            u_center_R[j] = Ubc[iR_cell - k + j][v];
            u_left_R[j]   = Ubc[iR_cell - 2*k + j][v];
            u_right_R[j]  = Ubc[iR_cell + j][v];
        }
        reconstruct_cell_legendre(u_center_R, u_left_R, u_right_R, wh_right[v],
                                  *co, m_epsilon, m_qPower);
    }

    // Zhang-Shu positivity limiter on right cell
    zhang_shu_limiter(wh_right, L, m_M);

    // Evaluate at left boundary (eta=0) of right cell
    for (int v = 0; v < nVars; ++v) {
        double derivs_R[MAX_L];
        eval_at_boundary(wh_right[v], L, co->EvalLeft, derivs_R, inv_dx);
        for (int d = 0; d < L; ++d)
            U_WenoR[d][v] = derivs_R[d];
    }
}


// ====================================================================
//  reconstruct_all_interfaces (allocating version)
// ====================================================================

void WENO1d::reconstruct_all_interfaces(
    const std::vector<Conserved>& U,
    std::vector<InterfacePoly>& PolyL,
    std::vector<InterfacePoly>& PolyR,
    WenoBCType bc
) const {
    const int nx = (int)U.size();
    const int Nghost = requiredGhost();

    std::vector<Conserved> Ubc_cons =
        (bc == WenoBCType::Periodic) ? make_periodic_Ubc(U, Nghost)
                                     : make_outflow_Ubc(U, Nghost);

    VectOfVectDouble Ubc = conserved_to_matrix(Ubc_cons);

    PolyL.assign(nx, InterfacePoly{});
    PolyR.assign(nx, InterfacePoly{});

    #pragma omp parallel for schedule(static)
    for (int i_interface = 0; i_interface < nx; ++i_interface) {
        VectOfVectDouble WL, WR;
        reconstruct_for_flux(Ubc, i_interface, WL, WR, Nghost);

        InterfacePoly left{}, right{};

        left.Q   = Conserved{WL[0][0], WL[0][1], WL[0][2]};
        right.Q  = Conserved{WR[0][0], WR[0][1], WR[0][2]};

        if (m_M >= 1) {
            left.Qx  = Conserved{WL[1][0], WL[1][1], WL[1][2]};
            right.Qx = Conserved{WR[1][0], WR[1][1], WR[1][2]};
        }
        if (m_M >= 2) {
            left.Qxx  = Conserved{WL[2][0], WL[2][1], WL[2][2]};
            right.Qxx = Conserved{WR[2][0], WR[2][1], WR[2][2]};
        }
        if (m_M >= 3) {
            left.Qxxx  = Conserved{WL[3][0], WL[3][1], WL[3][2]};
            right.Qxxx = Conserved{WR[3][0], WR[3][1], WR[3][2]};
        }
        if (m_M >= 4) {
            left.Qxxxx  = Conserved{WL[4][0], WL[4][1], WL[4][2]};
            right.Qxxxx = Conserved{WR[4][0], WR[4][1], WR[4][2]};
        }

        // Positivity-preserving fallback (allocating version)
        auto is_physical = [](const Conserved& Q) -> bool {
            if (Q.rho <= 0.0 || !std::isfinite(Q.rho)) return false;
            double p = (Gamma - 1.0) * (Q.E - 0.5 * Q.mom * Q.mom / Q.rho);
            return p > 0.0 && std::isfinite(p);
        };

        if (!is_physical(left.Q)) {
            int iL_ghost = i_interface + Nghost - 1;
            left.Q = Conserved{Ubc[iL_ghost][0], Ubc[iL_ghost][1], Ubc[iL_ghost][2]};
            left.Qx = left.Qxx = left.Qxxx = left.Qxxxx = Conserved{0,0,0};
        }

        if (!is_physical(right.Q)) {
            int iR_ghost = i_interface + Nghost;
            right.Q = Conserved{Ubc[iR_ghost][0], Ubc[iR_ghost][1], Ubc[iR_ghost][2]};
            right.Qx = right.Qxx = right.Qxxx = right.Qxxxx = Conserved{0,0,0};
        }

        PolyL[i_interface] = left;
        PolyR[i_interface] = right;
    }
}


// ====================================================================
//  helpers (same as before)

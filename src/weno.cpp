// ====================================================================
//  Core per-cell reconstruction: cell averages -> Legendre coefficients
// ====================================================================

// Compute oscillation indicator for a set of Legendre coefficients
// sigma = R_{lm} * w_l * w_m  (equation 18 of Dumbser 2008)
static double oscillation_indicator(const double wh[], int L, const double Rlm[][MAX_L]) {
    double sigma = 0.0;
    for (int l = 0; l < L; ++l) {
        for (int m = 0; m < L; ++m) {
            sigma += Rlm[l][m] * wh[l] * wh[m];
        }
    }
    return sigma;
}

// Reconstruct Legendre coefficients for one cell, one scalar variable
// u_stencil[0..nStencil-1] = cell averages on the stencil (centered on this cell)
// Output: wh[0..L-1] = WENO-combined Legendre coefficients
static void reconstruct_cell_legendre(
    const double u_center[],    // cell averages for central stencil [nS]
    const double u_left[],      // cell averages for left stencil [nS]
    const double u_right[],     // cell averages for right stencil [nS]
    double wh[],                // output: Legendre coefficients [L]
    const DumbserCoeffs& co,
    double epsilon, int r_power)
{
    const int L = co.L;
    const int nS = co.nStencil;

    // Step 1: Reconstruct on each stencil
    double wh_C[MAX_L] = {}, wh_L[MAX_L] = {}, wh_R[MAX_L] = {};
    for (int l = 0; l < L; ++l) {
        double vc = 0.0, vl = 0.0, vr = 0.0;
        for (int j = 0; j < nS; ++j) {
            vc += co.ReconC[l][j] * u_center[j];
            vl += co.ReconL[l][j] * u_left[j];
            vr += co.ReconR[l][j] * u_right[j];
        }
        wh_C[l] = vc;
        wh_L[l] = vl;
        wh_R[l] = vr;
    }

    // Step 2: Oscillation indicators for each stencil
    double sigma_C = oscillation_indicator(wh_C, L, co.Rlm);
    double sigma_L = oscillation_indicator(wh_L, L, co.Rlm);
    double sigma_R = oscillation_indicator(wh_R, L, co.Rlm);

    // Step 3: WENO-JS weights (Jiang, Shu, JCP 1996)
    //   alpha_s = lambda_s / (epsilon + sigma_s)^r
    const double lambda_C = 1.0e14;
    const double lambda_L = 1.0;
    const double lambda_R = 1.0;

    double alpha_C = lambda_C / std::pow(epsilon + sigma_C, r_power);
    double alpha_L = lambda_L / std::pow(epsilon + sigma_L, r_power);
    double alpha_R = lambda_R / std::pow(epsilon + sigma_R, r_power);

    double alpha_sum = alpha_C + alpha_L + alpha_R;
    double omega_C = alpha_C / alpha_sum;
    double omega_L = alpha_L / alpha_sum;
    double omega_R = alpha_R / alpha_sum;

    // Step 4: WENO-combined Legendre coefficients
    for (int l = 0; l < L; ++l) {
        wh[l] = omega_C * wh_C[l] + omega_L * wh_L[l] + omega_R * wh_R[l];
    }
}

// Evaluate interface derivatives from Legendre coefficients
// derivs[d] = (d^d/d_eta^d) w(eta_boundary) = sum_l Eval[d][l] * wh[l]
// Then convert to physical: Q_phys^(d) = derivs[d] / dx^d
static void eval_at_boundary(
    const double wh[], int L,
    const double Eval[][MAX_L],
    double derivs[],  // output: physical derivatives
    double inv_dx)
{
    double inv_dx_pow = 1.0;
    for (int d = 0; d < L; ++d) {
        double val = 0.0;
        for (int l = 0; l < L; ++l) {
            val += Eval[d][l] * wh[l];
        }
        derivs[d] = val * inv_dx_pow;
        inv_dx_pow *= inv_dx;
    }
}


// ====================================================================
//  Zhang-Shu positivity-preserving limiter
//  (Zhang & Shu, JCP 2010; "On positivity-preserving high order
//   discontinuous Galerkin schemes for compressible Euler equations")
//
//  Applied AFTER reconstruction, BEFORE boundary evaluation.
//  Modifies Legendre coefficients in-place to ensure:
//    (1) rho > 0   at all Gauss-Lobatto quadrature points in the cell
//    (2) p   > 0   at all Gauss-Lobatto quadrature points in the cell
//  Cell averages (wh[v][0]) are preserved exactly.
// ====================================================================

// Evaluate shifted Legendre polynomial P_l(xi) on [0,1] via recurrence
// P_0(xi) = 1,  P_1(xi) = 2*xi - 1,
// (n+1)*P_{n+1} = (2n+1)*(2*xi-1)*P_n - n*P_{n-1}
static double eval_shifted_legendre(int l, double xi) {
    if (l == 0) return 1.0;
    const double t = 2.0 * xi - 1.0;      // map [0,1] -> [-1,1]
    if (l == 1) return t;
    double Pnm1 = 1.0, Pn = t;
    for (int n = 1; n < l; ++n) {
        double Pnp1 = ((2*n + 1) * t * Pn - n * Pnm1) / (n + 1);
        Pnm1 = Pn;
        Pn   = Pnp1;
    }
    return Pn;
}

// Evaluate cell polynomial u(xi) = sum_{l=0}^{L-1} wh[l] * P_l(xi)
static double eval_cell_polynomial(const double wh[], int L, double xi) {
    double val = 0.0;
    for (int l = 0; l < L; ++l)
        val += wh[l] * eval_shifted_legendre(l, xi);
    return val;
}

// Gauss-Lobatto quadrature points on [0,1] for checking positivity.
// For polynomial degree M, we use M+2 GL points (endpoints + M interior).
static const int MAX_GL = 7;
struct GLPoints { int n; double xi[MAX_GL]; };

static GLPoints get_gauss_lobatto_points(int M) {
    GLPoints gl{};
    switch (M) {
    case 1:
        gl.n = 3;
        gl.xi[0] = 0.0;  gl.xi[1] = 0.5;  gl.xi[2] = 1.0;
        break;
    case 2:
        gl.n = 4;
        gl.xi[0] = 0.0;
        gl.xi[1] = 0.27639320225002103;   // (5-sqrt(5))/10
        gl.xi[2] = 0.72360679774997897;   // (5+sqrt(5))/10
        gl.xi[3] = 1.0;
        break;
    case 3:
        gl.n = 5;
        gl.xi[0] = 0.0;
        gl.xi[1] = 0.17267316464601141;   // (1-sqrt(3/7))/2
        gl.xi[2] = 0.5;
        gl.xi[3] = 0.82732683535398859;   // (1+sqrt(3/7))/2
        gl.xi[4] = 1.0;
        break;
    case 4:
        gl.n = 6;
        gl.xi[0] = 0.0;
        gl.xi[1] = 0.11747233803526763;   // GL6 interior nodes on [0,1]
        gl.xi[2] = 0.35738424175967748;
        gl.xi[3] = 0.64261575824032252;
        gl.xi[4] = 0.88252766196473237;
        gl.xi[5] = 1.0;
        break;
    default:  // M=0 or fallback: just check endpoints
        gl.n = 2;
        gl.xi[0] = 0.0;  gl.xi[1] = 1.0;
        break;
    }
    return gl;
}

// Zhang-Shu positivity-preserving limiter for one cell.
//   wh[3][MAX_L]: Legendre coefficients for (rho, m, E)
//   L:            number of Legendre coefficients (= M+1)
//   M:            polynomial degree
//
// After limiting, wh[v][0] (cell averages) are unchanged,
// and the polynomial satisfies rho > 0, p > 0 at all GL points.
static void zhang_shu_limiter(double wh[3][MAX_L], int L, int M) {
    const double rho_bar = wh[0][0];   // cell-average density
    const double m_bar   = wh[1][0];   // cell-average momentum
    const double E_bar   = wh[2][0];   // cell-average energy
    const double gm1     = Gamma - 1.0;

    // Pressure of cell average
    const double p_bar = gm1 * (E_bar - 0.5 * m_bar * m_bar / rho_bar);

    // If cell average itself is unphysical, flatten to piecewise constant
    if (rho_bar <= 0.0 || p_bar <= 0.0 ||
        !std::isfinite(rho_bar) || !std::isfinite(p_bar)) {
        for (int v = 0; v < 3; ++v)
            for (int l = 1; l < L; ++l)
                wh[v][l] = 0.0;
        return;
    }

    // Small positive floor: min(1e-13, cell_average)
    const double eps_rho = std::min(1.0e-13, rho_bar);
    const double eps_p   = std::min(1.0e-13, p_bar);

    // Nothing to limit for piecewise constant
    if (L <= 1) return;

    // Gauss-Lobatto evaluation points
    static const GLPoints gl1 = get_gauss_lobatto_points(1);
    static const GLPoints gl2 = get_gauss_lobatto_points(2);
    static const GLPoints gl3 = get_gauss_lobatto_points(3);
    static const GLPoints gl4 = get_gauss_lobatto_points(4);
    const GLPoints* gl = nullptr;
    switch (M) {
        case 1: gl = &gl1; break;
        case 2: gl = &gl2; break;
        case 3: gl = &gl3; break;
        case 4: gl = &gl4; break;
        default: gl = &gl1; break;
    }
    const int nGL = gl->n;

    // ----------------------------------------------------------------
    // Step 1: Density limiter
    //   Find min rho at GL points. If RhoMin < eps_rho, scale
    //   ALL higher-order coefficients by theta1 so that
    //   rho_limited(xi) = theta1*(rho(xi) - rho_bar) + rho_bar >= eps_rho.
    // ----------------------------------------------------------------
    double RhoMin = rho_bar;
    for (int q = 0; q < nGL; ++q) {
        double rho_q = eval_cell_polynomial(wh[0], L, gl->xi[q]);
        if (rho_q < RhoMin) RhoMin = rho_q;
    }

    if (RhoMin < eps_rho) {
        double theta1 = (rho_bar - eps_rho) / (rho_bar - RhoMin);
        if (theta1 < 0.0) theta1 = 0.0;
        if (theta1 > 1.0) theta1 = 1.0;
        // Scale ALL variables' higher-order coefficients by theta1
        for (int v = 0; v < 3; ++v)
            for (int l = 1; l < L; ++l)
                wh[v][l] *= theta1;
    }

    // ----------------------------------------------------------------
    // Step 2: Pressure limiter
    //   After density limiting, check pressure at GL points.
    //   If p < eps_p at any point, find theta2 via bisection such that
    //   blending toward cell average restores p >= eps_p.
    // ----------------------------------------------------------------
    double theta2 = 1.0;

    for (int q = 0; q < nGL; ++q) {
        double rho_q = eval_cell_polynomial(wh[0], L, gl->xi[q]);
        double m_q   = eval_cell_polynomial(wh[1], L, gl->xi[q]);
        double E_q   = eval_cell_polynomial(wh[2], L, gl->xi[q]);
        double p_q   = gm1 * (E_q - 0.5 * m_q * m_q / rho_q);

        if (p_q < eps_p) {
            // Bisect for theta in [0, 1]:
            //   U(theta) = theta * (U_q - U_bar) + U_bar
            //   p(theta) = (g-1)*(E(theta) - m(theta)^2/(2*rho(theta)))
            // At theta=0: p = p_bar > 0.  At theta=1: p = p_q < eps_p.
            double lo = 0.0, hi = 1.0;
            for (int iter = 0; iter < 50; ++iter) {
                double mid = 0.5 * (lo + hi);
                double r = mid * (rho_q - rho_bar) + rho_bar;
                double m = mid * (m_q   - m_bar)   + m_bar;
                double e = mid * (E_q   - E_bar)   + E_bar;
                double p = gm1 * (e - 0.5 * m * m / r);
                if (p < eps_p)
                    hi = mid;
                else
                    lo = mid;
            }
            if (lo < theta2) theta2 = lo;
        }
    }

    if (theta2 < 1.0) {
        for (int v = 0; v < 3; ++v)
            for (int l = 1; l < L; ++l)
                wh[v][l] *= theta2;
    }
}



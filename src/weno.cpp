// ====================================================================
//  WENO-Z reconstruction (Castro, Costa, Don, JCP 2011)
//
//  Framework: Dumbser et al. (JCP 2008) Legendre polynomial basis
//  Weights:   WENO-Z (Castro et al. 2011)
//
//  - Always 3 fixed stencils: left-shifted, central, right-shifted
//  - Reconstruction in Legendre polynomial basis on [0,1]
//  - Oscillation indicator: sigma = R_{lm} * w_l * w_m  (matrix form)
//  - Optimal linear weights: d_C = 2/3, d_L = d_R = 1/6  (sum to 1)
//  - WENO-Z: alpha_s = d_s * (1 + tau/(eps+sigma_s))^p, tau=|sigma_L-sigma_R|
//  - Reconstructs entire polynomial per cell (supplies spatial derivatives to CK)
// ====================================================================

static const int MAX_L = 5;   // max polynomial coefficients (M+1, M<=4)
static const int MAX_S = 5;   // max stencil size (2k+1, k<=2)

// Fast integer power
static inline double ipow_weno(double x, int n) {
    double r = 1.0;
    for (int i = 0; i < n; ++i) r *= x;
    return r;
}

